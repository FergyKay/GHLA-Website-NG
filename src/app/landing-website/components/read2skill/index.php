          <h2 class="text-center mb-5">Synopsis for Read2Skill</h2>
          <p class="lead">Ghana Library Authority, an agency under the Ministry of Education is mandated
            by Act 327 of 1970 to establish, equip, maintain and manage public libraries in Ghana.</p>

          <p class="lead">The vision of the Ghana Library Authority (GhLA) is to connect every Ghanaian to knowledge resources.</p>
          <p class="lead">The mission is to be a leading knowledge services hub in the sub region by connecting all citizenry to
            knowledge resources to radically improve literacy and development outcomes which transforms lives and
            communities.</p>
          <p class="lead">In line with our vision of connecting every Ghanaian to knowledge resources,the Ghana Library
            Authority has signed a partnership with Commonwealth of Learning (CoL) to roll out online learning for
            skills acquisition in Ghana.</p>
          <p class="lead">Commonwealth of Learning is the world’s only inter-governmental organizationand is solely concerned
            with:</p>
          <ul>
            <li>The promotion and development of distance education</li>
            <li>The promotion and development of open learning</li>
          </ul>

          <p class="lead">The project, named<strong>“Read2Skill”</strong> and will offer scholarship to one thousand (1000)
            Ghanaians in its first year through a competitive application process. The Read2Skill forms part of GhLA’s
            technology strategy to radically connect Ghanaians to learning resources through online learning
            platforms.</p>
          <p class="lead">Other commonwealth countries to benefit from this scholarship opportunity include:</p>
          <ul>
            <li>Kenya</li>
            <li>Rwanda</li>
            <li>Bangladesh</li>
          </ul>

          <p class="lead">The courses will be offered on the digital learning platform called Udemy. The objective of the
            course is:</p>
          <ul>
            <li>To provide a platform for self-paced learning for skill enhancement</li>
            <li>To build the human capital capacity of target group to use remote learning</li>
            <li>To prepare candidates for the job market or for self-employment</li>
          </ul>
