  <section class="content-section bg-light" id="faq">
    <div class="container ">
      <div class="row">
        <div class="col-lg-10 mx-auto">
          <h2 class="text-center mb-5">Frequently Asked Questions</h2>

          <p>1. What is Read2skill?</p>
          <p>Read2Skill is an online learning project by the Ghana Library Authority (GhLA) in collaboration with the Commonwealth of Learning (CoL) to offer opportunities for Ghanaians to acquire new skills through online learning on Udemy.
            <br>
            <br>
          </p>
          <p>2. What is Udemy?</p>
          <p>Udemy is an online learning and teaching marketplace with over 100,000 courses in a vast range of subjects.
            <br>
            <br>
          </p>
          <p>3. What is Commonwealth of Learning?</p>
          <p>Commonwealth of Learning (CoL) is the world’s only intergovernmental organization solely concerned with the promotion and development of distance education and open learning. It is hosted by the Government of Canada and headquartered in Burnaby, Canada.
            <br>
            <br>
          </p>
          <p>4. How many courses are available?</p>
          <p>The subject areas include Data Science, Cloud Computing, Project Management, IT Operations, Design, Finance and Accounting, Human Resource, Leadership and Management, Office Productivity, Marketing and Sales and many others.
            <br>
            <br>
          </p>
          <p>&nbsp;5. How can I apply?</p>
          <p>You can apply online via
            <a href="http://www.ghanalibrary.org/read2skill"><u>www.ghanalibrary.org/read2skill</u></a><u>.</u> You can visit any of our Regional Libraries across the country and also be assisted by our Read2Skill Coordinators.
            <br>
            <br>
          </p>
          <p>6. Who qualifies?</p>
          <p>Any Ghanaian above 15 who wants to acquire skills through online learning. &nbsp;They include but not limited to:</p>
          <ul>
            <li>Unemployed graduates in search of skills</li>
            <li>Informal and formal sector employees</li>
            <li>Professionals</li>
            <li>High school graduates</li>
            <li>University students
              <br>
              <br>
            </li>
          </ul>
          <p>&nbsp;7. Does my application guarantee enrolment?</p>
          <p>No. Enrollment would be based on ‘first come first served.’ Only shortlisted applicants would be contacted.
            <br>
            <br>
          </p>
          <p>8. Where will the training take place?</p>
          <p>All learning will take place online.
            <br>
            <br>
          </p>
          <p>9. When will the application be opened?</p>
          <p>Applications would be opened on 1<sup>st</sup> January and end on 31<sup>st</sup> January, 2020.
            <br>
            <br>
          </p>
          <p>&nbsp;10. What is the duration for the project?</p>
          <p>&nbsp; &nbsp; &nbsp; Read2skill is a yearlong project. However, the duration of each course differs.
            <br>
            <br>
          </p>
          <p>&nbsp;11. Can I read more than one course?</p>
          <p>&nbsp;Yes. You can even read more than one course at a go. For Instance, you can read Project Management and Digital Marketing within the same period.
            <br>
            <br>
          </p>
          <p>12. Will certificates be awarded at the end of the programme?</p>
          <p>Yes, certificates would be awarded after each course.
            <br>
            <br>
          </p>
          <p>13. Is this open to just Ghanaians? Can foreigners living in Ghana apply?&nbsp;</p>
          <p>No. The scholarship opportunity is opened to just Ghanaian nationals.
            <br>
            <br>
          </p>
          <p>14. Do I have to pay?</p>
          <p>Enrolling a course on Udemy through Ghana Library Authority is fully paid for by the Commonwealth of Learning. However, you are responsible for your internet expenses to access the course online.</p>


